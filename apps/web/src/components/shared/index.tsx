export function policyEffectLabel(policy: Policy) {
  switch (policy.effect) {
    case "BLOCK":
      return "Block";
    case "REQUIRE_APPROVAL":
      return "Approval";
    case "VALIDATE":
      return "Validate";
    default:
      return policy.effect;
  }
}

export function toneForOutcome(
  outcome?: string,
): "ok" | "warn" | "danger" | "neutral" {
  if (outcome === "ALLOWED" || outcome === "APPROVED") return "ok";
  if (outcome === "PENDING_APPROVAL") return "warn";
  if (outcome === "BLOCKED" || outcome === "ERROR" || outcome === "DENIED")
    return "danger";
  return "neutral";
}

export function matchedRules(log: ToolLog) {
  if (log.error) return log.error;
  if (!log.matchedRulesJson?.length) return "Default policy";
  return log.matchedRulesJson.map((rule) => rule.name ?? rule.effect).join(", ");
}

export function prettyJson(value: Record<string, unknown>) {
  const text = JSON.stringify(value);
  return text.length > 120 ? `${text.slice(0, 117)}…` : text;
}
