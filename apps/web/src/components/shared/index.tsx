export function Badge(props: {
  children: React.ReactNode;
  variant?: string;
  className?: string;
}) {
  const cls = [
    "badge",
    props.variant ? `badge--${props.variant.toLowerCase().replace(/_/g, "-")}` : "",
    props.className ?? "",
  ]
    .filter(Boolean)
    .join(" ");

  return <span className={cls}>{props.children}</span>;
}

export function Metric(props: {
  icon: React.ReactNode;
  label: string;
  value: number | string;
  tone?: "ok" | "warn" | "danger" | "neutral";
}) {
  return (
    <article className={`metric metric--${props.tone ?? "neutral"}`}>
      <div className="metric__icon">{props.icon}</div>
      <div className="metric__body">
        <span className="metric__label">{props.label}</span>
        <strong className="metric__value">{props.value}</strong>
      </div>
    </article>
  );
}

